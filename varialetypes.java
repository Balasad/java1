public class VariableTypesDemo {
    public static void main(String[] args) {
        
        int integerVar = 10;          // Integer type variable
        double doubleVar = 3.14;     // Double type variable
        char charVar = 'A';          // Character type variable
        boolean boolVar = true;      // Boolean type variable
        String stringVar = "Hello";   // String type variable (corrected quotation mark)
        
        System.out.println("Integer variable: " + integerVar);
        System.out.println("Double variable: " + doubleVar);
        System.out.println("Character variable: " + charVar);
        System.out.println("Boolean variable: " + boolVar);
        System.out.println("String variable: " + stringVar);
    }
}
